#include "libft.h"

void		*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t x;

	x = 0;
	while (x < n)
	{
		((char*)dst)[x] = ((char*)src)[x];
		x++;
	}
	return (dst);
}
