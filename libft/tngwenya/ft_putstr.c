#include "libft.h"

void		ft_putstr(const char *str)
{
	int x;

	x = 0;
	if (!str)
		return ;
	while (((char*)str)[x] != '\0')
	{
		write(1, &((char*)str)[x], 1);
		x++;
	}
}
