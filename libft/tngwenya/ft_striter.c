#include "libft.h"

void		ft_striter(char *s, void (*f)(char*))
{
	int x;

	x = 0;
	if (s != NULL && f != NULL)
	{
		while (s[x] != '\0')
		{
			f(&s[x]);
			x++;
		}
	}
}
