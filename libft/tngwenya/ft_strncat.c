#include "libft.h"

char			*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t x;
	size_t y;

	x = 0;
	y = 0;
	while (s1[x] != '\0')
	{
		x++;
	}
	while (s2[y] != '\0' && (y < n))
	{
		s1[x] = s2[y];
		x++;
		y++;
	}
	s1[x] = '\0';
	return (s1);
}
